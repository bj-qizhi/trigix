/**
 * Translate a panel label/option/hint to the active locale.
 * Returns the original English when no translation exists (brand/technical fields).
 */
export function fl(en: string): string {
  if (_loc !== 'zh') return en
  // Normalize internal whitespace (codemod-captured fragments may carry source
  // newlines/indentation) so dictionary keys stay clean single-spaced strings.
  const k = en.replace(/\s+/g, ' ').trim()
  if (SPAN_ZH[k]) return SPAN_ZH[k]
  if (LABEL_ZH[k]) return LABEL_ZH[k]

  // Strip a trailing required marker ` *`.
  let req = ''
  let s = k
  if (s.endsWith(' *')) {
    req = ' *'
    s = s.slice(0, -2)
  }
  if (LABEL_ZH[s]) return LABEL_ZH[s] + req

  // Translate "Base (paren)" compositionally: translate base + inner if known.
  const m = s.match(/^(.*?)\s*\(([^()]*)\)$/)
  if (m) {
    const base = LABEL_ZH[m[1]]
    if (base) {
      const inner = PAREN_ZH[m[2]] ?? m[2]
      return `${base}（${inner}）${req}`
    }
  }
  return en
}
