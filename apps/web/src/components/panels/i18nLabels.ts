// Localized field labels for the node config panels.
//
// The config panels render hundreds of `<label>` / `<option>` strings as literal
// English. Threading `locale` through ~140 component call-sites would be huge
// churn, so instead the editor root sets a module-scoped locale once per render
// (the panels only ever render inside the editor subtree) and labels call `fl()`.
//
// `fl()` translates *generic* terms via LABEL_ZH and falls back to the original
// English for anything not in the dictionary — which is exactly what we want for
// brand-specific / API-parameter field names (Slack channel, Stripe amount, …),
// per product decision: keep those technical names in English.

import type { Locale } from '../../i18n'

let _loc: Locale = 'zh'

/** Set by the editor root each render so the whole panel subtree shares one locale. */
export function setLabelLocale(l: Locale): void {
  _loc = l
}

export function labelLocale(): Locale {
  return _loc
}

